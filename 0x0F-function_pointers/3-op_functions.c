/**
 * op_add - add to int.
 *
 * @a: the first number.
 * @b: the second number.
 *
 * Return: the result
 */

int     op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two int.
 *
 * @a: the first number.
 * @b: the second number.
 *
 * Return: the result
 */

int     op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplicate two int.
 *
 * @a: the first number.
 * @b: the second number.
 *
 * Return: the result
 */

int     op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divid two int.
 *
 * @a: the first number.
 * @b: the second number.
 *
 * Return: the result
 */

int     op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int.
 *
 * @a: the first number.
 * @b: the second number.
 *
 * Return: the result
 */

int     op_mod(int a, int b)
{
	return (a % b);
}
